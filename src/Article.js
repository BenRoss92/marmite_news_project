function Article(articleObject) {
  this.id = articleObject.id;
  this.sectionId = articleObject.sectionId;
  this.sectionName = articleObject.sectionName;
  this.webPublicationDate = articleObject.webPublicationDate;
  this.webTitle = articleObject.webTitle;
  this.webUrl = articleObject.webUrl;
  this.apiUrl = articleObject.apiUrl;
}
